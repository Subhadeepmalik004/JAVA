package strings;

public class BuiltInStringMethods {
    public static void main(String[] args) {
        String s = "Subhadeep Malik";
        System.out.println(s.charAt(3));  // h
        System.out.println(s.charAt(9)); // space
        int n = s.length();
        System.out.println(n);  // 15
    }
}
