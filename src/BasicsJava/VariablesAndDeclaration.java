package BasicsJava;

public class VariablesAndDeclaration {
    public static void main(String[] args) {
        int a;//Declaration
        a = 2;//Initialization
        System.out.println(a);
        int x = 6;//Declaration and Initialization
        System.out.println(x);
        x = 7;
        System.out.println(x);
        x = x + 6;
        System.out.println(x);
        x = x -6;
        System.out.println(x);
        x = x * 2;
        System.out.println(x);
        x = x/2;
        System.out.println(x);
    }
}
