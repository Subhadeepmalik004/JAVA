package BasicsJava;

public class IntByInt {
    public static void main(String[] args) {
        //first calculate then store
        double x = 5/2;// 5/2 -->>(int / int) === 2.0 -->>(int)
        //first 5/2 calculate then value is store "double x"
        System.out.println(x);
        //first store then calculate
        double a = 5;// 5 is double
        double b = 2;// 2 is double
        System.out.println(a/b);// (a/b) -->>(double/double) === 2.5 -->>(double)

    }
}
